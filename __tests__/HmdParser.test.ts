import {parseHmd, serializeHmd, HmdParseResult} from "../src/data/HmdParser";

// ─── §3 Frontmatter ─────────────────────────────────────────────────

describe("3. Frontmatter", () => {
	it("Example 1: Minimal frontmatter", () => {
		const input = `---\nblockId: "1992306556"\n---\n`;
		const result = parseHmd(input);
		expect(result.settings.blockId).toBe("1992306556");
		expect(result.settings.autoSave).toBe(true);
		expect(result.settings.header).toBe("");
		expect(result.settings.footer).toBe("");
		expect(result.units).toHaveLength(0);
	});

	it("Example 2: Full frontmatter", () => {
		const input = [
			"---",
			'blockId: "1992306556"',
			"autoSave: false",
			'header: "<b>World History</b>"',
			'footer: "<i>Generated by Historica</i>"',
			"---",
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result.settings.blockId).toBe("1992306556");
		expect(result.settings.autoSave).toBe(false);
		expect(result.settings.header).toBe("<b>World History</b>");
		expect(result.settings.footer).toBe("<i>Generated by Historica</i>");
	});
});

// ─── §4 Entry ────────────────────────────────────────────────────────

describe("4. Entry", () => {
	it("Example 3: Basic entry", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## July 14, 1789",
			"date:: 1789-07-14",
			"source:: flow-test.md",
			"",
			"The French Revolution began on July 14, 1789.",
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result.units).toHaveLength(1);
		const u = result.units[0];
		expect(u.parsedResultText).toBe("July 14, 1789");
		expect(u.time.style).toBe("unix");
		expect(/^-?\d+$/.test(u.time.value)).toBe(true);
		expect(u.filePath).toBe("flow-test.md");
		expect(u.sentence).toBe("The French Revolution began on July 14, 1789.");
		expect(u.isExpanded).toBe(false);
		expect(u.attachments).toHaveLength(0);
	});

	it("Example 4: Entry with internal state", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## September 1, 1939",
			"date:: 1939-09-01",
			"source:: user-test-ww2.md",
			"%%id: 2044819733%%",
			"%%hidden%%",
			"",
			"The war in Europe began when Germany invaded Poland on September 1, 1939.",
			"Britain and France declared war on Germany two days later.",
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result.units).toHaveLength(1);
		const u = result.units[0];
		expect(u.parsedResultText).toBe("September 1, 1939");
		expect(u.id).toBe("2044819733");
		expect(u.isHidden).toBe(true);
		expect(u.sentence).toBe(
			"The war in Europe began when Germany invaded Poland on September 1, 1939.\n" +
			"Britain and France declared war on Germany two days later."
		);
	});

	it("Example 5: Free-form date", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## The Bronze Age",
			"date:: circa 3300 BCE",
			"date-style:: free",
			"",
			"The Bronze Age began around 3300 BCE in the Near East.",
			"",
		].join("\n");
		const result = parseHmd(input);
		const u = result.units[0];
		expect(u.time.style).toBe("free");
		expect(u.time.value).toBe("circa 3300 BCE");
	});

	it("Example 6: Unix timestamp", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## Apollo 11",
			"date:: -14182940000",
			"source:: space-history.md",
			"",
			"Humans first walked on the Moon on July 20, 1969.",
			"",
		].join("\n");
		const result = parseHmd(input);
		const u = result.units[0];
		expect(u.time.style).toBe("unix");
		expect(u.time.value).toBe("-14182940000");
		expect(u.filePath).toBe("space-history.md");
	});
});

