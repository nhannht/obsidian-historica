// ─── §8 Edge cases ──────────────────────────────────────────────────

describe("8. Edge cases", () => {
	it("Example 11: Multi-paragraph body", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## The Renaissance",
			"date:: 1400-01-01",
			"",
			"The Renaissance was a cultural movement that began in Italy.",
			"",
			"It spread across Europe over the following centuries, fundamentally",
			"changing art, science, and philosophy.",
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result.units[0].sentence).toBe(
			"The Renaissance was a cultural movement that began in Italy.\n" +
			"\n" +
			"It spread across Europe over the following centuries, fundamentally\n" +
			"changing art, science, and philosophy."
		);
	});

	it("Example 12: Entry with no source field", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## Some Event",
			"date:: 2023-01-15",
			"",
			"A manually added entry with no source file.",
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result.units[0].filePath).toBe("");
	});

	it("Example 13: Markdown formatting in body", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## The Moon Landing",
			"date:: 1969-07-20",
			"source:: space.md",
			"",
			'Neil Armstrong said: *"That\'s one small step for man, one giant leap',
			'for mankind."* The **Apollo 11** mission was a success.',
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result.units[0].sentence).toContain('*"That\'s one small step');
		expect(result.units[0].sentence).toContain("**Apollo 11**");
	});

	it("Example 14: Empty file (frontmatter only)", () => {
		const input = `---\nblockId: "999"\n---\n`;
		const result = parseHmd(input);
		expect(result.settings.blockId).toBe("999");
		expect(result.units).toHaveLength(0);
	});

	it("Example 15: Unknown frontmatter fields preserved", () => {
		const input = [
			"---",
			'blockId: "100"',
			'customField: "hello"',
			"---",
			"",
			"## Test",
			"date:: 2020-01-01",
			"",
			"Body text.",
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result._extraFrontmatter).toBeDefined();
		expect(result._extraFrontmatter).toContainEqual(["customField", "hello"]);
	});

	it("Example 16: Unknown comment fields preserved", () => {
		const input = [
			"---",
			'blockId: "100"',
			"---",
			"",
			"## Test",
			"date:: 2020-01-01",
			"%%id: abc%%",
			"%%customTag: some value%%",
			"",
			"Body text.",
			"",
		].join("\n");
		const result = parseHmd(input);
		expect(result.units[0].id).toBe("abc");
		expect(result._extraComments?.get("abc")).toContainEqual("%%customTag: some value%%");
	});
});

// ─── Round-trip ─────────────────────────────────────────────────────

describe("Round-trip", () => {
	const roundTripCases: [string, string][] = [
		["single entry", [
			"---",
			'blockId: "100"',
			"autoSave: true",
			'header: ""',
			'footer: ""',
			"---",
			"",
			"## Apollo 11",
			"date:: -14182940000",
			"source:: space-history.md",
			"%%id: abc123%%",
			"",
			"Humans first walked on the Moon on July 20, 1969.",
			"",
		].join("\n")],
		["two entries", [
			"---",
			'blockId: "100"',
			"autoSave: true",
			'header: ""',
			'footer: ""',
			"---",
			"",
			"## Event One",
			"date:: 946684800000",
			"%%id: e1%%",
			"",
			"The first event.",
			"",
			"---",
			"",
			"## Event Two",
			"date:: 978307200000",
			"%%id: e2%%",
			"",
			"The second event.",
			"",
		].join("\n")],
		["entry with free date", [
			"---",
			'blockId: "100"',
			"autoSave: true",
			'header: ""',
			'footer: ""',
			"---",
			"",
			"## The Bronze Age",
			"date:: circa 3300 BCE",
			"date-style:: free",
			"%%id: bronze%%",
			"",
			"The Bronze Age began around 3300 BCE in the Near East.",
			"",
		].join("\n")],
		["entry with attachments and hidden", [
			"---",
			'blockId: "100"',
			"autoSave: true",
			'header: ""',
			'footer: ""',
			"---",
			"",
			"## D-Day",
			"date:: -804556800000",
			"source:: ww2-notes.md",
			"%%id: dday%%",
			"%%hidden%%",
			"attachments:",
			"- [[images/d-day-beach.jpg]]",
			"",
			"On June 6, Allied forces landed.",
			"",
		].join("\n")],
	];

	it.each(roundTripCases)("parse then serialize is stable: %s", (_name, input) => {
		const parsed = parseHmd(input);
		const serialized = serializeHmd(parsed);
		const reparsed = parseHmd(serialized);
		const reserialized = serializeHmd(reparsed);
		// Idempotent: second serialization matches first
		expect(reserialized).toBe(serialized);
	});

	it.each(roundTripCases)("structural equality after round-trip: %s", (_name, input) => {
		const parsed = parseHmd(input);
		const serialized = serializeHmd(parsed);
		const reparsed = parseHmd(serialized);

		expect(reparsed.settings).toEqual(parsed.settings);
		expect(reparsed.units.length).toBe(parsed.units.length);
		for (let i = 0; i < parsed.units.length; i++) {
			expect(reparsed.units[i].parsedResultText).toBe(parsed.units[i].parsedResultText);
			expect(reparsed.units[i].sentence).toBe(parsed.units[i].sentence);
			expect(reparsed.units[i].time).toEqual(parsed.units[i].time);
			expect(reparsed.units[i].filePath).toBe(parsed.units[i].filePath);
			expect(reparsed.units[i].id).toBe(parsed.units[i].id);
			expect(reparsed.units[i].isHidden).toBe(parsed.units[i].isHidden);
			expect(reparsed.units[i].attachments.map((a: any) => a.path))
				.toEqual(parsed.units[i].attachments.map((a: any) => a.path));
		}
	});
});
